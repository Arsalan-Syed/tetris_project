def make_chart(data, label, fname):
    plt.title(label)
    plt.plot([d[0] for d in data])
    plt.plot([d[1] for d in data])
    plt.plot([d[2] for d in data])
    plt.legend(['Worst', 'Average', 'Best'])
    plt.ylabel('Score')
    plt.xlabel('Generation')
    plt.grid(True)
    plt.savefig(fname)

